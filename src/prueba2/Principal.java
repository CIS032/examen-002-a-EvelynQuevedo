    private void jButtIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtIngresarActionPerformed
        ListaProfesores lista = new ListaProfesores();
        String nombres = Nombres.getText();
        String apellidos = Apellido.getText();
        int cedula = Integer.parseInt(Cedula.getText());
        String carrera = Carrera.getText();
        int tipo = Integer.parseInt(tiempoPar.getText());
        if (tipo == 0) {
            int horasClase = Integer.parseInt(Horasclase.getText());
            if (horasClase > 2 & horasClase < 17) {
                lista.agregar(new ProfesorTiempoCompleto(cedula, nombres, apellidos, horasClase, carrera));
            } else {
                JOptionPane.showMessageDialog(null, "El docente de tiempo completo debe impartir desde 3 horas hasta 16 horas clase", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
        if (tipo == 1) {
            int horasClase = Integer.parseInt(Horasclase.getText());
            if (horasClase == 10) {
                lista.agregar(new ProfesorMedioTiempo(cedula, nombres, apellidos, horasClase, carrera));
            } else {
                JOptionPane.showMessageDialog(null, "El docente de medio tiempo debe impartir 10 horas clase", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
        if (tipo == 2) {
            int horascom = Integer.parseInt(hComplementarias.getText());
            int horasClase = Integer.parseInt(Horasclase.getText());
            if ((horasClase > 1 & horasClase < 10) & horascom < 20) {
                lista.agregar(new ProfesorTiempoParcial(cedula, nombres, apellidos, horasClase, carrera, horascom));
            } else {
                JOptionPane.showMessageDialog(null, "El docente de tiempo parcial trabaja menos de 20 horas a la semana y debe impartir de 2 a 9 horas clase.", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
        ListaProfesores.grabar();
        Apellido.setText("");
        Carrera.setText("");
        Cedula.setText("");
        Nombres.setText("");
     
        Horasclase.setText("");
        hComplementarias.setText("");
        

    }//GEN-LAST:event_jButtIngresarActionPerformed

